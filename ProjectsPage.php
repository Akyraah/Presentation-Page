		<div class="navHeader2"> Modelling choices </div>
		<div class="navInner">
			<span class="tab"></span>Given the complexity of the environment in which an airline company evolves, I've made several choices to simplify the problem
			in order to be able to develop a model which makes an optimization possible.<br>
			<span class="tab"></span>In that sense, I used a graph to represent airports and the different flights available to the company. The amount of people taking the plane on one connection
			at one specific time is represented by a variable named demand. This demand decreases as flights are scheduled on the connection.
		</div>
	</div>
	<div class="navSection">
		<img src="Pics/TIPE2.png" alt="TIPE2" style="width:25%; float:left">
		<div class="navHeader2"> Optimization </div>
		<div class="navInner">
		
			<span class="tab"></span>Once the model is created, I implemented an algorithm that decides, at each moment, whether a plane should fly towards a specific airport depending on the current demand and planes' availability.<br>
			<img src="Pics/TIPE4.png" alt="TIPE4" style="width:25%; float:right">
			<span class="tab"></span>Afterwards, I modified the algorithm to forecast moment where it would be beneficial for the company to make flight that are not not beneficial in order to position a plane at the start of an highly beneficial connection.<br>
			<span class="tab"></span>Finally, the algorithm checks if a small modification of the schedule would be beneficial for the company and keeps the modification when it is.
		</div>
